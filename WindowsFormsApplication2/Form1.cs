                    noron2 = egitimveriler[satir, 1];
                    noron3 = egitimveriler[satir, 2];
                    noron4 = egitimveriler[satir, 3];
                    cks = egitimveriler[satir, 4];



                    cikisHesapla(noron1, noron2, noron3, noron4, cks);

                }
                sayac++;

                lepoksay.Text = sayac.ToString() + " . epok bitti. Devam Ediyor..";

                if (egitimYaptımı > 0)
                {
                    egitimYaptımı = 0;
                }
                else
                {
                    lsayac.Text = sayac.ToString() + ". epokta tamamlandı.";
                    break;
                }

            }
        }

